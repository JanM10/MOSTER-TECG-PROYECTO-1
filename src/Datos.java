public class Datos {
    public int id;
    public String nombre;
    public int daño;
    public int coste;
    public int vida;
    public int hechizo;
    public int cartas;
    public int robar;

    public int getRobar() {
        return robar;
    }

    public void setRobar(int id) {
        this.robar = robar;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getDaño() {
        return daño;
    }

    public void setDaño(int daño) {
        this.daño = daño;
    }

    public int getCoste() {
        return coste;
    }

    public void setCoste(int coste) {
        this.coste = coste;
    }

    public int getVida() {
        return vida;
    }

    public void setVida(int vida) {
        this.vida = vida;
    }

    public int getHechizo() {
        return hechizo;
    }

    public void setHechizo(int hechizo) {
        this.hechizo = hechizo;
    }

    public int getCartas() {
        return cartas;
    }

    public void setCartas(int cartas) {
        this.cartas = cartas;
    }
}
